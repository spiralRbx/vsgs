local function LGDLWLD_fake_script() -- Fake Script: StarterGui.SPIMENU.SPIFRAME1.LocalScript0
    local script = Instance.new("LocalScript")
    script.Name = "LocalScript0"
    script.Parent = Converted["_SPIFRAME1"]
    local req = require
    local require = function(obj)
        local fake = fake_module_scripts[obj]
        if fake then
            return fake()
        end
        return req(obj)
    end

	local UserInputService = game:GetService("UserInputService")
	
	local frame = script.Parent
	
	local dragging = false
	local dragInput
	local dragStart
	local startPos
	
	local function update(input)
		local delta = input.Position - dragStart
		frame.Position = UDim2.new(
			startPos.X.Scale, startPos.X.Offset + delta.X,
			startPos.Y.Scale, startPos.Y.Offset + delta.Y
		)
	end
	
	frame.InputBegan:Connect(function(input)
		if input.UserInputType == Enum.UserInputType.MouseButton1 or input.UserInputType == Enum.UserInputType.Touch then
			dragging = true
			dragStart = input.Position
			startPos = frame.Position
	
			-- Ativa o modo modal para prevenir toque através do frame
			UserInputService.ModalEnabled = true
	
			input.Changed:Connect(function()
				if input.UserInputState == Enum.UserInputState.End then
					dragging = false
					-- Desativa o modo modal quando o toque termina
					UserInputService.ModalEnabled = false
				end
			end)
		end
	end)
	
	frame.InputChanged:Connect(function(input)
		if input.UserInputType == Enum.UserInputType.MouseMovement or input.UserInputType == Enum.UserInputType.Touch then
			dragInput = input
		end
	end)
	
	UserInputService.InputChanged:Connect(function(input)
		if dragging and input == dragInput then
			update(input)
		end
	end)
	
	-- Previne o movimento da câmera ao arrastar o frame em dispositivos móveis
	UserInputService.TouchStarted:Connect(function(input, processed)
		if not processed and input.UserInputType == Enum.UserInputType.Touch then
			local target = input.Target
			if target and target:IsDescendantOf(frame) then
				UserInputService.ModalEnabled = true
			end
		end
	end)
	
	UserInputService.TouchEnded:Connect(function(input, processed)
		if input.UserInputType == Enum.UserInputType.Touch then
			UserInputService.ModalEnabled = false
		end
	end)
	
end

